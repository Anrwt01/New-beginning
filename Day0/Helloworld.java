package Day0;
public class Helloworld{
public static void main(String[] args) {
   
// for printing square 4*4
   int n=4;

   for ( int i=n; i>=1; i--){

    for ( int k=1; k<=(n-i); k++){
      System.out.print( " " );
    }

    for ( int j=i; j>=1; j--){
    System.out.print("*");}

    System.out.println();
     
   }
        }

        }
    
     
     

       
  

